alert("En este programa se calculara el promedio de la edad de 4 personas");
let p1 = parseInt(prompt("Por favor digitar la edad de la primera persona"));
let p2 = parseInt(prompt("Por favor digitar la edad de la segunda persona"));
let p3 = parseInt(prompt("Por favor digitar la edad de la tercera persona"));
let p4 = parseInt(prompt("Por favor digitar la edad de la cuarta persona"));
let promedio = (p1+p2+p3+p4)/4;
alert("El promedio de las edades es" + " " + promedio);